print("calculadora")



n_1 = int(input('Digite Um Numero: '))
n_2 = int(input('Digite Um Novo Numero: '))

#adicao
print('{} + {} = '.format(n_1, n_2))
print(n_1 + n_2)

#subtrair
print('{} - {} = '.format(n_1, n_2))
print(n_1 - n_2)

#comomultilplicar
print('{} * {} = '.format(n_1, n_2))
print(n_1 * n_2)

#paradividir
print('{} / {} = '.format(n_1, n_2))
print(n_1 / n_2)